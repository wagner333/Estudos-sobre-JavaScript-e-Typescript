/*
let é uma palavra-chave usada para declarar variáveis em JavaScript.
 Ela foi introduzida no ECMAScript 6 (ES6) e 
é uma alternativa ao var, que era a forma tradicional de declarar variáveis antes do ES6.
A principal diferença entre let e var é o escopo.
 Variáveis declaradas com let têm escopo de bloco, o que significa que elas 
só estão acessíveis dentro do bloco de código onde foram declaradas.
 Por outro lado, variáveis declaradas com var têm escopo de função, o que significa que elas
  estão acessíveis em toda a função onde foram declaradas.
*/

let string = "Hello, World!";
let number = 42;
let boolean = true;
let array = [1, 2, 3];
let object = { name: "John", age: 30 };
let undefinedVariable;
let nullVariable = null;
