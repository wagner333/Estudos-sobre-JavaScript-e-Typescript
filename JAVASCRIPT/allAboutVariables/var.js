/*
o var é o tipo de declaração de variavel mais antiga do javascript
ele tem um escopo global ou de função, o que significa que se você declarar uma variável usando var 
dentro de uma função, ela estará disponível em toda a função, mas não fora dela.
Se você declarar uma variável usando var fora de qualquer função, ela se torna uma variável global
e pode ser acessada de qualquer lugar no código.
*/

var string = "Hello, World!";
var number = 42;
var boolean = true;
var array = [1, 2, 3];
var object = { name: "John", age: 30 };
var undefinedVariable;
var nullVariable = null;
