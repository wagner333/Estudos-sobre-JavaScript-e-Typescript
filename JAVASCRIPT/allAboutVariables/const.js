/*
Constantes em JavaScript 
Constantes são variáveis cujo valor não pode ser alterado após a sua atribuição inicial.
 Elas são declaradas usando a palavra-chave const.
 */

const name = "wagner";
const age = 30;
const isStudent = true;
const hobbies = ["reading", "coding", "traveling"];
const address = { city: "New York", country: "USA" };
const undefinedVariable = undefined;
const nullVariable = null;

